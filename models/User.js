const mongoose = require("mongoose");
const { Schema } = mongoose;

const userSchema = new Schema({
  accountId: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  age: {
    type: Number,
    required: true
  },
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  }
});

mongoose.model("users", userSchema);
